    "CREATE TEMP TABLE _fitapp_import_mesocycle_map (old_id INTEGER PRIMARY KEY, new_id INTEGER NOT NULL);",
    "CREATE TEMP TABLE _fitapp_import_microcycle_map (old_id INTEGER PRIMARY KEY, new_id INTEGER NOT NULL);",
