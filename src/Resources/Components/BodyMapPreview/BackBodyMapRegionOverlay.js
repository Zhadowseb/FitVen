const DEFAULT_PRIMARY_COLOR = "#60DAAC";
const DEFAULT_SECONDARY_COLOR = "#18A06C";

function normalizeRegionKey(value) {
  return typeof value === "string"
    ? value.trim().toLocaleLowerCase().replace(/[^a-z0-9]+/g, "_")
    : "";
}

function getUniqueRegionKeys(regionKeys) {
  const seenKeys = new Set();

  return (regionKeys ?? []).reduce((keys, regionKey) => {
    const normalizedKey = normalizeRegionKey(regionKey);

    if (!normalizedKey || seenKeys.has(normalizedKey)) {
      return keys;
    }

    if (!BACK_REGION_PATHS[normalizedKey]) {
      return keys;
    }

    seenKeys.add(normalizedKey);
    keys.push(normalizedKey);
    return keys;
  }, []);
}

function renderRegionPaths(regionKeys, { color, layer, opacity }) {
  return regionKeys.flatMap((regionKey) =>
    BACK_REGION_PATHS[regionKey].map((path) => (
      <Path
        key={`${layer}-${regionKey}-${path.id}`}
        d={path.d}
        fill={color}
        fillOpacity={opacity}
      />
    ))
  );
}

export default function BackBodyMapRegionOverlay({
  height = "100%",
  primaryColor = DEFAULT_PRIMARY_COLOR,
  primaryOpacity = 0.72,
  primaryRegionKeys = [],
  secondaryColor = DEFAULT_SECONDARY_COLOR,
  secondaryOpacity = 0.5,
  secondaryRegionKeys = [],
  style,
  width = "100%",
}) {
  const primaryKeys = getUniqueRegionKeys(primaryRegionKeys);
  const primaryKeySet = new Set(primaryKeys);
  const secondaryKeys = getUniqueRegionKeys(secondaryRegionKeys).filter(
    (regionKey) => !primaryKeySet.has(regionKey)
  );

  if (primaryKeys.length === 0 && secondaryKeys.length === 0) {
    return null;
  }

  return (
    <Svg
      height={height}
      pointerEvents="none"
      preserveAspectRatio="none"
      style={style}
      viewBox={`0 0 ${BODY_WIDTH} ${BODY_HEIGHT}`}
      width={width}
    >
      {renderRegionPaths(secondaryKeys, {
        color: secondaryColor,
        layer: "secondary",
        opacity: secondaryOpacity,
      })}
      {renderRegionPaths(primaryKeys, {
        color: primaryColor,
        layer: "primary",
        opacity: primaryOpacity,
      })}
    </Svg>
  );
}
